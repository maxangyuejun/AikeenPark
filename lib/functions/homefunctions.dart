import 'package:aikeen_park/screens/home.dart';

int choosingNumCarparks(int numCarparksValue) {
  if (numCarparksValue == 1) {
    return 5;
  }
  if (numCarparksValue == 2) {
    return 10;
  }
  if (numCarparksValue == 3) {
    return 15;
  }
  if (numCarparksValue == 4) {
    return 20;
  }
  return 0;
}
